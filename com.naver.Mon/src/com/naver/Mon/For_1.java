package com.naver.Mon;

public class For_1 {

	public static void main(String[] args) {
		//Hello World 3번 출력
		for(int i = 1; i < 11; i = i + 1) {
			System.out.println(i);
		}
		
		//image1.png~3 까지
		for(int j=1;j<4;j=j+1) {
			System.out.println("image" + j + ".png");
		}
		int a = 0;
		while(a<11) {
			System.out.println(a);
			a = a + 1;
		}
		
	}

}
